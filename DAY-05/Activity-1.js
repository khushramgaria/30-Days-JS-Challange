// Task 1: Write a function to check if a number is even or odd and log the result to the console.

function evenOrOdd(num) {
    if (num % 2 === 0) {
        console.log(num + " is even number !!")
    } else {
        console.log(num + " is odd number !!")
    }
}

evenOrOdd(4)

